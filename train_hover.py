def run(config_path: str):
    config = load_config(config_path)

    name = config["name"]
    hypothesis = config["hypothesis"]
    budget = config["budget_seconds"]
    ppo_kwargs = config["ppo"]
    reward_code = config["reward_code"]

    # ── Paths ─────────────────────────────────────────────────────────────────
    lab_dir = os.path.dirname(os.path.abspath(__file__))
    results_dir = os.path.join(lab_dir, "results", name)
    os.makedirs(results_dir, exist_ok=True)

    # Copy config to results dir for reproducibility
    import shutil
    shutil.copy2(config_path, os.path.join(results_dir, "config.yaml"))

    print(f"\n{'='*65}")
    print(f"  EXPERIMENT: {name}")
    print(f"  HYPOTHESIS: {hypothesis}")
    print(f"  BUDGET:     {budget}s")
    print(f"  CONFIG:     {config_path}")
    print(f"  OUTPUT:     {results_dir}")
    print(f"{'='*65}\n")

    # ── Build environment class from reward code ──────────────────────────────
    CustomHoverAviary = make_hover_aviary_class(reward_code)

    env_kwargs = dict(obs=ObservationType.KIN, act=ActionType.ONE_D_RPM)
    train_env = make_vec_env(CustomHoverAviary, env_kwargs=env_kwargs,
                             n_envs=1, seed=0)
    eval_env = CustomHoverAviary(**env_kwargs)

    print(f"[INFO] Action space:      {train_env.action_space}")
    print(f"[INFO] Observation space: {train_env.observation_space}")
    print(f"[INFO] Reward code:\n{reward_code}\n")

    # ── Model ─────────────────────────────────────────────────────────────────
    model = PPO("MlpPolicy", train_env, verbose=1, **ppo_kwargs)

    # ── Callbacks ─────────────────────────────────────────────────────────────
    time_cb = TimeBudgetCallback(budget_seconds=budget)
    eval_cb = EvalCallback(
        eval_env,
        verbose=1,
        best_model_save_path=results_dir,
        log_path=results_dir,
        eval_freq=1000,
        deterministic=True,
        render=False,
    )
    step_log_path = os.path.join(results_dir, "steps.csv")
    step_cb = StepLoggerCallback(log_path=step_log_path)

    # ── Train ─────────────────────────────────────────────────────────────────
    wall_start = time.time()
    model.learn(
        total_timesteps=int(1e8),
        callback=[time_cb, eval_cb, step_cb],
        log_interval=100,
    )
    elapsed = time.time() - wall_start

    # ── Evaluate best model ───────────────────────────────────────────────────
    best_model_path = os.path.join(results_dir, "best_model.zip")
    if os.path.exists(best_model_path):
        best_model = PPO.load(best_model_path)
        mean_reward, std_reward = evaluate_policy(
            best_model, eval_env, n_eval_episodes=10
        )
    else:
        print("[WARN] No best_model.zip found — using final model for eval.")
        mean_reward, std_reward = evaluate_policy(
            model, eval_env, n_eval_episodes=10
        )

    train_env.close()
    eval_env.close()

    # ── Save metrics ──────────────────────────────────────────────────────────
    metrics = {
        "experiment": name,
        "backend": "hover",
        "hypothesis": hypothesis,
        "config_file": os.path.basename(config_path),
        "timestamp": datetime.now().isoformat(),
        "elapsed_seconds": round(elapsed, 1),
        "mean_reward": round(float(mean_reward), 3),
        "std_reward": round(float(std_reward), 3),
        "timesteps_trained": int(model.num_timesteps),
        "ppo_kwargs": ppo_kwargs,
        "reward_code": reward_code.strip(),
    }

    metrics_path = os.path.join(results_dir, "metrics.json")
    with open(metrics_path, "w") as f:
        json.dump(metrics, f, indent=2)
    print(f"\n[Saved] {metrics_path}")

    # ── Print summary ─────────────────────────────────────────────────────────
    print(f"\n{'='*65}")
    print(f"  RESULTS")
    print(f"  mean_reward  = {mean_reward:.3f} ± {std_reward:.3f}")
    print(f"  timesteps    = {model.num_timesteps:,}")
    print(f"  wall time    = {elapsed:.1f}s")
    print(f"{'='*65}\n")

    # ── Write outbox file ─────────────────────────────────────────────────────
    outbox_dir = os.path.join(lab_dir, "outbox")
    os.makedirs(outbox_dir, exist_ok=True)
    outbox_path = os.path.join(outbox_dir, f"{name}.md")
    with open(outbox_path, "w") as f:
        f.write(f"# {name} — Results\n\n")
        f.write(f"**Hypothesis:** {hypothesis}\n\n")
        f.write(f"| Metric | Value |\n|--------|-------|\n")
        f.write(f"| mean_reward | {mean_reward:.3f} ± {std_reward:.3f} |\n")
        f.write(f"| timesteps_trained | {model.num_timesteps:,} |\n")
        f.write(f"| wall_time | {elapsed:.1f}s |\n\n")
        f.write(f"*(Linux Claude: write full analysis to "
                f"`results/{name}/EXPERIMENT.md`, then update this file.)*\n")
    print(f"[Updated] {outbox_path}")

    print(f"\nNext steps for Linux Claude:")
    print(f"  1. Write results/{name}/EXPERIMENT.md")
    print(f"  2. git add -A && git commit -m '{name}: <summary>'")
    print(f"  3. Update outbox/{name}.md with your analysis\n")

    return metrics


