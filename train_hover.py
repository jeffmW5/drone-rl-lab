def load_config(config_path: str) -> dict:
    """Load experiment config from a YAML file."""
    with open(config_path, "r") as f:
        config = yaml.safe_load(f)

    required = ["name", "hypothesis", "reward_code", "ppo"]
    for field in required:
        if field not in config:
            raise ValueError(f"Config missing required field: {field}")

    config.setdefault("budget_seconds", 180)
    config.setdefault("backend", "hover")
    return config


# =============================================================================
# CUSTOM ENVIRONMENT — reward function loaded from config
# =============================================================================

def make_hover_aviary_class(reward_code: str):
    """
    Dynamically create a CustomHoverAviary class with the given reward code.

    The reward_code string has access to:
      - self: the environment instance
      - state: drone state vector (position, orientation, velocity, etc.)
      - np: numpy
      - dist: distance from target (pre-computed convenience variable)

    Example reward_code:
      dist = np.linalg.norm(self.TARGET_POS_CUSTOM - state[0:3])
      return max(0, 2 - dist**4)
    """
    # Process reward code: convert final "return X" to "__return__ = X"
    reward_lines = reward_code.strip().split("\n")
    for i in range(len(reward_lines) - 1, -1, -1):
        stripped = reward_lines[i].strip()
        if stripped.startswith("return "):
            reward_lines[i] = reward_lines[i].replace("return ", "__return__ = ", 1)
            break

    processed_code = "\n".join(reward_lines)

    class CustomHoverAviary(HoverAviary):
        TARGET_POS_CUSTOM = np.array([0.0, 0.0, 1.0])

        def _computeReward(self):
            state = self._getDroneStateVector(0)
            dist = np.linalg.norm(self.TARGET_POS_CUSTOM - state[0:3])
            local_vars = {
                "self": self, "state": state, "dist": dist,
                "np": np, "max": max, "min": min, "abs": abs,
                "__return__": 0.0,
            }
            exec(processed_code, local_vars)
            return local_vars["__return__"]

    return CustomHoverAviary


