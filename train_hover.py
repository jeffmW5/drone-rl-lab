class TimeBudgetCallback(BaseCallback):
    """Stops training when the wall-clock budget is exceeded."""

    def __init__(self, budget_seconds: int, verbose: int = 0):
        super().__init__(verbose)
        self.budget_seconds = budget_seconds
        self.start_time: float = 0.0

    def _on_training_start(self) -> None:
        self.start_time = time.time()

    def _on_step(self) -> bool:
        elapsed = time.time() - self.start_time
        if elapsed > self.budget_seconds:
            print(f"\n[TimeBudget] {self.budget_seconds}s elapsed — stopping training.")
            return False
        return True


class StepLoggerCallback(BaseCallback):
    """
    Logs per-step metrics during training.
    Records: timestep, distance, velocity, z_pos, episode number.
    Writes to a CSV file at end of training.
    """

    def __init__(self, log_path: str, verbose: int = 0):
        super().__init__(verbose)
        self.log_path = log_path
        self.rows = []
        self.episode_count = 0
        # Log every Nth step to keep file size reasonable
        self.log_every = 10

    def _on_step(self) -> bool:
        if self.num_timesteps % self.log_every != 0:
            return True

        try:
            base_env = self.training_env.envs[0].unwrapped
            state = base_env._getDroneStateVector(0)
            pos = state[0:3]
            vel = state[10:13]
            target = np.array([0.0, 0.0, 1.0])

            self.rows.append({
                "timestep": self.num_timesteps,
                "distance": round(float(np.linalg.norm(target - pos)), 6),
                "velocity": round(float(np.linalg.norm(vel)), 6),
                "z_pos": round(float(pos[2]), 6),
                "episode": self.episode_count,
            })

            dones = self.locals.get("dones", [False])
            if any(dones):
                self.episode_count += 1

        except Exception:
            pass  # Don't crash training if logging fails

        return True

    def _on_training_end(self) -> None:
        self._save()

    def _save(self):
        if not self.rows:
            return
        with open(self.log_path, "w", newline="") as f:
            writer = csv.DictWriter(
                f, fieldnames=["timestep", "distance", "velocity", "z_pos", "episode"]
            )
            writer.writeheader()
            writer.writerows(self.rows)
        print(f"[StepLogger] Wrote {len(self.rows)} rows to {self.log_path}")


